SELECT * FROM employees WHERE salary > 10000; -- Greater than
SELECT * FROM employees WHERE salary < 10000; -- Less than
SELECT * FROM employees WHERE salary >= 10000; -- Greater than or equal to
SELECT * FROM employees WHERE salary <= 10000; -- Less than or equal to
SELECT * FROM employees WHERE salary = 10000; -- Equal to
SELECT * FROM employees WHERE salary <> 10000; -- Not equal to
SELECT * FROM employees WHERE salary != 10000; -- Not equal to
SELECT * FROM employees WHERE salary !< 10000; -- Not less than
SELECT * FROM employees WHERE salary !> 10000; -- Not greater than

